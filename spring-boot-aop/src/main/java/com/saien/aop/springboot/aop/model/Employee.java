package com.saien.aop.springboot.aop.model;

/**
 * @author Paiker Abbas
 *
 */
public class Employee {

	private Integer empId;
	private String name;

	public Employee() {
		super();
	}

	public Integer getEmpId() {
		return empId;
	}

	public void setEmpId(Integer empId) {
		this.empId = empId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
